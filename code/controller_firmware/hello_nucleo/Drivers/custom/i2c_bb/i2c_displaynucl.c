void updateEncoder_LCD(UART_HandleTypeDef * huart, uint16_t data_in)
{
	int i = 0;
	uint8_t ascii_data_in[5] = {0x48,0x48,0x48,0x48,0x48};
	while(data_in > 0)
	{
		ascii_data_in[i] = (data_in % 10) + 48;
		data_in = data_in / 10;
		++i;
	}

	uint8_t data_Send[20] = {0xFE, 0x45, 0x09, ascii_data_in[4], 0xFE, 0x45, 0x10, ascii_data_in[3], 0xFE, 0x45, 0x11, ascii_data_in[2], 0xFE, 0x45, 0x12, ascii_data_in[1], 0xFE, 0x45, 0x13, ascii_data_in[0]};
	huart->gState = HAL_UART_STATE_READY;
	HAL_UART_Transmit(huart, data_Send, 20,100);
}
