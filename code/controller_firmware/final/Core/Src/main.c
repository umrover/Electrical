void HAL_I2C_SlaveRxCpltCallback(I2C_HandleTypeDef * hi2c) {

	static uint8_t prev_en = 0;
	static uint8_t filt_count;

	/**
	 * Critical section:
	 * - Process UI command into global struct
	 * - Update FSM atomically
	 * - Set points update automatically
	 */
	__disable_irq();

	control_packet_transform();

	/* UI Handler */

	/* Update FSM state */
	if (currentControl.ENABLE && !prev_en)
	{
		FSM_Transition(&mc_state, STARTUP);
	}
	else if (!currentControl.ENABLE && prev_en)
	{
		FSM_Transition(&mc_state, FAULT);
	}


	/*
	 * END Critical Section
	 */
	__enable_irq();

	__HAL_I2C_CLEAR_FLAG(hi2c, I2C_FLAG_ADDR);
	HAL_I2C_EnableListen_IT(hi2c);
}


void HAL_I2C_AddrCallback(I2C_HandleTypeDef * hi2c, uint8_t TransferDirection, uint16_t AddrMatchCode)
{
	HAL_I2C_DisableListen_IT(hi2c);

	if (TransferDirection == I2C_DIRECTION_TRANSMIT)
	{
        I2C_DriverRead(hi2c);
	}
	else
	{
        I2C_DriverTransmit(hi2c, currentTelemetry);
	}

	HAL_I2C_EnableListen_IT(hi2c);
}


